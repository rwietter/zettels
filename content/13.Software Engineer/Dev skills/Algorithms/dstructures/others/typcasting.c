#include <stdio.h>

int main(int argc, char const *argv[])
{
	float valor = 5.78;
	int valorINT = (int)valor;
	printf("Valor = %f, ValorINT = %d\n", valor, valorINT);
	int value = 5;
	float valueFLOAT = (float)value;
	printf("Valor = %d, ValorFLOAT = %f", value, valueFLOAT);

	return 0;
}
